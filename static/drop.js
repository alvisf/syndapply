var expanded = false;

function showChecktypes() {
    var checkboxes = document.getElementById("types");
    if (!expanded) {
        checkboxes.style.display = "block";
        expanded = true;
    } else {
        checkboxes.style.display = "none";
        expanded = false;
    }
}

